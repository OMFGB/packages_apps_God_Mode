package com.t3hh4xx0r.addons.alerts;

public class AlertsObject {
	
	private String mURL;
	private String mDevice;
	private String mName;
	private String mZipName;
	private String mInstallable;
	private String mDescription;
	private String mCategory;
	private String mDensity;
	
	public void setURL(String mURL) {
		this.mURL = mURL;
	}
	public String getURL() {
		return mURL;
	}
	public void setDevice(String mDevice) {
		this.mDevice = mDevice;
	}
	public String getDevice() {
		return mDevice;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	public String getName() {
		return mName;
	}
	public void setZipName(String mZipName) {
		this.mZipName = mZipName;
	}
	public String getZipName() {
		return mZipName;
	}
	public void setInstallable(String mInstallable) {
		this.mInstallable = mInstallable;
	}
	public String getInstallable() {
		return mInstallable;
	}
	public void setDescription(String mDescription) {
		this.mDescription = mDescription;
	}
	public String getDescription() {
		return mDescription;
	}
	public void setCategory(String mCategory) {
		this.mCategory = mCategory;
	}
	public String getCategory() {
		return mCategory;
	}
	public void setDensity(String mDensity) {
		this.mDensity = mDensity;
	}
	public String getDensity() {
		return mDensity;
	}

}
